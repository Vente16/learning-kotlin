fun main() {
    val number: Int = 10
    val longNumber: Long = 10000000L
    val doubleNumber: Double = 3.14
    val floatNumber: Float = 3.14F
    val boolean: Boolean = true
    val string: String = "Hello World"
    val char: Char = 'J'

    println("Number datatype: $number")
    println("Long datatype: $longNumber")
    println("Double datatype: $doubleNumber")
    println("Float datatype: $floatNumber")
    println("Boolean datatype: $boolean")
    println("String datatype: $string")
    println("Char datatype: $char")
}