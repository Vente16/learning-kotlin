fun main() {
    val isAdult = true
    val isMale = false
    // and operator "&&" / "and"
    println(isAdult && isMale)
    // or operator "||" / "or"
    println(isAdult || isMale)
    // inverting the boolean value
    println(!isAdult) // since it is true is going to be false
    println(!isMale) // true
}